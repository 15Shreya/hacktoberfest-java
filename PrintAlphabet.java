package org.example.basics;

/*
     Write a program to print alphabet from n to n.
*/




class Print_Alphabet
{
public static void main(String args[])
{
 for(char i='a';i<='z';i++)
{
 System.out.print(i+" ");
}

System.out.println();

for(char i='A';i<='Z';i++)
{
 System.out.print(i+" ");
}
}
}
